# Here I am showing python conditions.

# if conditions:

a = 10
b = 7
if a > b:
    print("a is greater than b")


# elif conditions:
a = 10
b = 7
if a < b:
    print("a is less than b")
elif b < a:
    print("b is less than a")

# else conditions:
a = 10
b = 7
if a == b:
    print("a equal b")
elif b == a:
    print("b equal a")
else:
    print("a and b are different")