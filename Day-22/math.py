# Here i am showing built-in math modules.

# Min, Max mathod
x = min(5, 10, 25)
y = max(5, 10, 25)
print(x)
print(y)

# Absolute function
x = abs(-7.25)
print(x)

# Power function
x = pow(4, 3)
print(x)

# Built-In Math
import math

x = math.sqrt(64)       # Square root ```sqrt``
print(x)

x = math.ceil(1.4)      # Number Upwards 
y = math.floor(1.4)     # Number Downwards
print(x) 
print(y) 

x = math.pi             # Value of PI
print(x)