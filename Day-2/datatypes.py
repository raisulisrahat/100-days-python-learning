# Here I am showing python datatypes.

"""
String: "Hellow World"
Integer: 15, 0, -5
Float: 1.45
Bool: True, False
Complex: 1f, 0+1.4j
Tuple: ("A", "B", "C")
Set: {"A", "B", "C"}
List: ["A", "B", "C"]
Dictionary: {"A":"a", "B":"b", "C":"c"}
"""

